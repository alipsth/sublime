    private void TampilData() {
     DefaultTableModel model=new DefaultTableModel();
         model.addColumn("Nim");
         model.addColumn("Nama_Siswa");
         model.addColumn("Jenis_Kelamin");
         model.addColumn("Jurusan");
         model.addColumn("Semester");
         tabel.setModel(model);
         //menampilkan data database kedalam tabel
         try{
            java.sql.Statement stat=koneksi2.GetConnection().createStatement();
            ResultSet data= stat.executeQuery
            ("SELECT * FROM mahasiswa");
            while(data.next()){
               model.addRow(new Object[]{
               data.getString("NIM"),
               data.getString("Nama_Siswa"),
               data.getString("jenis_kelamin"),
               data.getString("Jurusan"),
               data.getString("Semester")             
               });
               tabel.setModel(model);
            }
         }
         catch(Exception e){
            System.err.println("terjadi kesalahan" +e );
        }
    }
