    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Apakah anda ingin menghapus data ini?",
            "Konfirmasi Dialog",JOptionPane.YES_NO_OPTION);
        if(ok == 0) {
            String sql="DELETE FROM mahasiswa WHERE Nim='"+ nim.getText().trim()+"'";
            try{
                java.sql.Statement stat = koneksi2.GetConnection().createStatement();
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Data Berhasil di Hapus", "Hapus Data"
                    ,JOptionPane.INFORMATION_MESSAGE);
                stat.close();
                nim.setText("");
                nama.setText("");
                jurusan.setSelectedIndex(-0);
                semester.setText("");
                nim.requestFocus();
                TampilData();
            }
            catch (SQLException exc) {
                System.err.println(sql);
                System.err.println("Error : "+exc);
            }
        }
    }//GEN-LAST:event_hapusActionPerformed

    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        String Nim = nim.getText();
        String Nama = nama.getText();
        String jeniskelamin = null;
        if(laki.isSelected()){
            jeniskelamin="laki-laki";
        }
        else if(perempuan.isSelected()){
            jeniskelamin="perempuan";
        }
        String jurus=jurusan.getSelectedItem().toString();
        String nama1=nama.getText();
        String nim1=nim.getText();
        String semester1=semester.getText();
        if(nim.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"mohon masukan data untuk di input",
                "PERHATIAN",JOptionPane.WARNING_MESSAGE);
        }

        else{
            try{
                java.sql.Statement stat =koneksi2.GetConnection().createStatement();
                ResultSet data= stat.executeQuery("SELECT*FROM mahasiswa "
                    + "WHERE nim ='"+nim.getText()+"'");
                if(data.next()){
                    JOptionPane.showMessageDialog(null, "nim sudah ada","PERHATIAN",
                        JOptionPane.WARNING_MESSAGE);
                    nim.requestFocus();
                }
                else{
                    String sql="INSERT INTO mahasiswa VALUES ('"+nim1+"'"
                    +",'"+nama1+"'"
                    +",'"+jeniskelamin+"'"
                    +",'"+jurus+"'"
                    +",'"+semester1+"')" ;
                    stat.executeUpdate(sql);
                    nim.setText("");
                    nama.setText("");
                    jurusan.setSelectedIndex(-0);
                    semester.setText("");
                    nim.requestFocus();
                    JOptionPane.showMessageDialog(null, "data berhasil disimpan","SUKSES",
                        JOptionPane.INFORMATION_MESSAGE);
                    TampilData();
                }
                stat.close();
            }catch (Exception exc){
                System.out.println("Terjadi Kesalahan :" + exc);
            }
        }
    }//GEN-LAST:event_simpanActionPerformed

