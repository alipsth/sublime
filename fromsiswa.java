    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();
        if (baris != -1) {
            nim.setText(tabel.getValueAt(baris, 0).toString());
            nama.setText(tabel.getValueAt(baris, 1).toString());
            if ("Laki-Laki".equals (tabel.getValueAt(baris, 1).toString())){
                laki.setSelected(true);
            }
            else {
                perempuan.setSelected(true);
            }
            jurusan.setSelectedItem(tabel.getValueAt(baris, 3).toString());
            semester.setText(tabel.getValueAt(baris, 4).toString());
        }
    }//GEN-LAST:event_tabelMouseClicked

